var corpo = document.getElementById("corpo")

function red() {
    if(corpo.classList.contains('red')) {
        corpo.classList.remove('red');
    } else {
        corpo.style.transitionDuration = ".5s";
        corpo.className = '';
        corpo.classList.add('red');
    }
}

